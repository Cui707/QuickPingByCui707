  if (vendor == null) return '';
  if (vendor == 'Apple') return 'Apple';
  const phones = {'Samsung','Xiaomi','Huawei','OPPO','vivo','OnePlus','Google','Motorola','Sony','HTC','ZTE','Realme'};
  const routers = {'TP-Link','D-Link','Netgear','Asus','Ubiquiti','MikroTik','Belkin','Tenda','Cisco'};
