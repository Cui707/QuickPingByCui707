/// Returns vendor name from MAC OUI (first 3 bytes).
String? lookupOui(String? mac) {
  if (mac == null || mac.length < 8) return null;
  final n = mac.replaceAll('-', ':').toUpperCase();
  return ouiDb[n.substring(0, 8)];
}

/// Returns device category based on vendor name.
